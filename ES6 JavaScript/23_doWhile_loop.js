/*


? do...while Loop in JavaScript
 *The do...while loop is a variant of the while loop, but it ensures that the code block inside the loop is executed at least once, regardless of whether the condition is true or false. 
 *This is because the condition is checked after the code block has been executed.





*/

let i = 1
do {
    console.log(i)
    i++
} while (i <= 5);